/* 

20.- Write a program that prints a Christmas tree:

		 /\
		/  \
	   /    \
	  /      \
      --------
		 " "
		 " "
		 " "
		
Remember to use escape sequences.

*/

public class ChristmasTreeEscapeSequencePrinter {
	
	public static void main(String[] args) {
		
		System.out.println("\n    /\\");
		System.out.println("   /  \\");
		System.out.println("  /    \\");
		System.out.println(" /      \\");
		System.out.println(" --------");
		System.out.println("   \"  \" ");
		System.out.println("   \"  \" ");
		System.out.println("   \"  \" ");
		
		System.exit(0);
		
	}
	
}
