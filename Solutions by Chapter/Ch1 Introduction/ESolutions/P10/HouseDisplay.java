/* 

10.- Write a program that prints a house that looks exactly like the following:

   +
  + +
 +   +
+-----+
| .-. |
| | | |
+-+-+-+

*/

public class HouseDisplay {
	
	public static void main(String[] args){
		System.out.println("     +    ");
		System.out.println("    + +   ");
		System.out.println("   +   +  ");
		System.out.println("  +-----+ ");
		System.out.println("  | .-. | ");
		System.out.println("  | | | | ");
		System.out.println("  +-+-+-+ ");
		System.exit(0);
	}
	
}